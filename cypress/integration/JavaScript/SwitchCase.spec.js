describe('Javascript Basics',()=>{


    it('SwitchCase',()=>{

        let letters = 'i'
        switch(letters)
        {
        case 'a':
        console.log( 'Vowel a')
        break;

        case 'e':
       console.log( 'Vowel e')
        break;

        case 'i':
       console.log( 'Vowel i')
        break;

        case 'o':
       console.log( 'Vowel o')
        break;


        case 'u':
       console.log( 'Vowel u')
        break;
    
default:

console.log( 'Not vowels')
}
   
    } )

} )
